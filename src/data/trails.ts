export type Priority = 'alta' | 'media' | 'baixa'
export type TopicType = 'conceito' | 'pratica' | 'quiz'

export interface Topic {
  id: string
  index: number
  title: string
  priority: Priority
  type: TopicType
  description: string
  practiceHint: string
  projectContext: string
  xp: number
}

export interface Trail {
  id: string
  name: string
  slug: string
  icon: string // emoji
  color: string // hex accent
  colorGlow: string // hsl glow
  description: string
  topics: Topic[]
}

const xpMap: Record<Priority, number> = { alta: 30, media: 20, baixa: 10 }

function t(
  id: string,
  index: number,
  title: string,
  priority: Priority,
  description: string,
  practiceHint: string,
  projectContext: string,
): Topic {
  return {
    id,
    index,
    title,
    priority,
    type: priority === 'alta' ? 'conceito' : priority === 'media' ? 'pratica' : 'quiz',
    description,
    practiceHint,
    projectContext,
    xp: xpMap[priority],
  }
}

